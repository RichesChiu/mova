const interpolate = (template: string, params?: TranslationParams) => {
  if (!params) {
    return template
  }

  return template.replace(/\{\{(.*?)\}\}/g, (_match, rawKey: string) => {
    const key = rawKey.trim()
    const value = params[key]
    return value === null || value === undefined ? '' : String(value)
  })
}

export const translateForLanguage = (
  language: InterfaceLanguage,
  message: string,
  params?: TranslationParams,
) => {
  const template = language === 'zh-CN' ? (zhCN[message] ?? message) : message
  return interpolate(template, params)
}

export const getCurrentInterfaceLanguage = (): InterfaceLanguage => {
  if (typeof document !== 'undefined') {
    return normalizeInterfaceLanguagePreference(document.documentElement.lang)
  }

  return readStoredInterfaceLanguagePreference()
}

export const translateCurrent = (message: string, params?: TranslationParams) =>
  translateForLanguage(getCurrentInterfaceLanguage(), message, params)
