create or replace function mova_notify_user_change()
returns trigger
language plpgsql
as $$
declare
    changed_user_id bigint;
begin
    if tg_op = 'DELETE' then
        changed_user_id := old.id;
    else
        changed_user_id := new.id;
    end if;
    perform mova_bump_realtime_revision('admin:users');
    perform mova_bump_realtime_revision('user:' || changed_user_id || ':profile');

    if tg_op = 'DELETE' then
        perform mova_bump_realtime_revision('session:user:' || changed_user_id);
    elsif tg_op = 'UPDATE'
          and (new.role is distinct from old.role
               or new.is_enabled is distinct from old.is_enabled
               or new.password_hash is distinct from old.password_hash) then
        perform mova_bump_realtime_revision('session:user:' || changed_user_id);
    end if;
    if tg_op = 'DELETE' then
        return old;
    end if;
    return new;
end;
$$;

