                                                resource_key text primary key,
    revision bigint not null default 1,
    updated_at timestamptz not null default now()
    );

create or replace function mova_bump_realtime_revision(target_resource_key text)
returns void
language plpgsql
as $$
begin
    insert into realtime_revisions (resource_key, revision, updated_at)
    values (target_resource_key, 1, now())
    on conflict (resource_key) do update
        set revision = realtime_revisions.revision + 1,
            updated_at = excluded.updated_at;

    perform pg_notify('mova_realtime', target_resource_key);
end;
$$;

create or replace function mova_notify_library_change()
returns trigger
language plpgsql
as $$
declare
    changed_library_id bigint;
begin
    if tg_op = 'DELETE' then
        changed_library_id := old.id;
    else
        changed_library_id := new.id;
    end if;
    if tg_op = 'INSERT' then
        perform mova_bump_realtime_revision('admin:libraries');
    elsif tg_op = 'UPDATE' then
        perform mova_bump_realtime_revision('library:' || changed_library_id || ':settings');
    else
        perform mova_bump_realtime_revision('admin:libraries');
        perform mova_bump_realtime_revision('library:' || changed_library_id || ':settings');
    end if;
    if tg_op = 'DELETE' then
        return old;
    end if;
    return new;
end;
$$;

create trigger libraries_realtime_revision
after insert or update or delete on libraries
for each row execute function mova_notify_library_change();

create or replace function mova_notify_catalog_change()
returns trigger
language plpgsql
as $$
declare
    changed_library_id bigint;
begin
    if current_setting('mova.defer_catalog_revision', true) = 'on' then
        if tg_op = 'DELETE' then
            return old;
        end if;
        return new;
    end if;

    if tg_op = 'DELETE' then
        changed_library_id := old.library_id;
    else
        changed_library_id := new.library_id;
    end if;
    perform mova_bump_realtime_revision('library:' || changed_library_id || ':catalog');
    if tg_op = 'DELETE' then
        return old;
    end if;
    return new;
end;
$$;

