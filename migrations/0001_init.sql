create or replace function mova_notify_series_catalog_change()
returns trigger
language plpgsql
as $$
declare
    changed_series_id bigint;
    changed_library_id bigint;
begin
    if current_setting('mova.defer_catalog_revision', true) = 'on' then
        if tg_op = 'DELETE' then
            return old;
        end if;
        return new;
    end if;

    if tg_op = 'DELETE' then
        changed_series_id := old.series_id;
    else
        changed_series_id := new.series_id;
    end if;
    select library_id into changed_library_id
    from media_items
    where id = changed_series_id;

    if changed_library_id is not null then
        perform mova_bump_realtime_revision('library:' || changed_library_id || ':catalog');
    end if;
    if tg_op = 'DELETE' then
        return old;
    end if;
    return new;
end;
$$;

