create table if not exists notifications (
                                             id bigint generated by default as identity primary key,
    category varchar(32) not null,
    notification_type varchar(64) not null,
    severity varchar(16) not null,
    audience varchar(16) not null,
    library_id bigint references libraries(id) on delete cascade,
    user_id bigint references users(id) on delete cascade,
    source_key text not null unique,
    payload jsonb not null default '{}'::jsonb,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    check (severity in ('info', 'success', 'warning', 'error')),
    check (audience in ('server', 'admin', 'library', 'user')),
    check (
        (audience in ('server', 'admin') and library_id is null and user_id is null)
        or (audience = 'library' and library_id is not null and user_id is null)
        or (audience = 'user' and library_id is null and user_id is not null)
    )
    );

