create table if not exists media_files (
                                           id bigint generated by default as identity primary key,
                                           library_id bigint not null,
    media_item_id bigint not null,
    file_path text not null,
    container varchar(64),
    file_size bigint not null,
    duration_seconds integer,
    video_title text,
    video_codec varchar(64),
    video_profile varchar(128),
    video_level varchar(32),
    audio_codec varchar(64),
    width integer,
    height integer,
    bitrate bigint,
    video_bitrate bigint,
    video_frame_rate double precision,
    video_aspect_ratio varchar(32),
    video_scan_type varchar(32),
    video_color_primaries varchar(64),
    video_color_space varchar(64),
    video_color_transfer varchar(64),
    video_bit_depth integer,
    video_pixel_format varchar(64),
    video_reference_frames integer,
    technical_tags text[] not null default '{}'::text[],
    local_analysis_version integer not null default 1,
    scan_hash varchar(128),
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    unique(library_id, file_path),
    foreign key (media_item_id, library_id)
        references media_items(id, library_id)
        on delete cascade
    );

create index if not exists idx_media_files_library_id on media_files(library_id);
create index if not exists idx_media_files_media_item_id on media_files(media_item_id);

create table if not exists subtitle_files (
